package com.bridgelabz.OOPS.Model;

public class InventoryManModel {
	private long weight;
	private long price;
	private long value;
	private long grand;
	public long getWeight() {
		return weight;
	}
	public void setWeight(long weight) {
		this.weight = weight;
	}
	public long getPrice() {
		return price;
	}
	public void setPrice(long price) {
		this.price = price;
	}
	public long getValue() {
		return value;
	}
	public void setValue(long value) {
		this.value = value;
	}
	public long getGrand() {
		return grand;
	}
	public void setGrand(long grand) {
		this.grand = grand;
	}

}
